// Types of literals
let a = "10"; // String Literal
let b = 10; // Number Literal
let c = true; // Boolean Literal
let d = null; // Null Literal
let e = undefined; // Undefined Literal
let f = {}; // Object Literal
let g = []; // Array Literal
let h = 10.5; // Float Literal
let i = 10; // Integer Literal
let j = true; // Boolean Literal
let k = Symbol("10"); // Symbol Literal
let l = BigInt(10); // BigInt Literal
let m = NaN; // NaN Literal
let n = Infinity; // Infinity Literal 
// template literal
let o = `10`;
console.log(o);